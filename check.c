#include "./philo.h"

int	check(char **av)
{
	int i;
	int j;

	j = 1;
	while(av[j])
	{
		i = 0;
		while(av[j][i])
		{
			if(av[j][i] > 47 && av[j][i] < 58)
				i++;
			else
				return (1);
		}
		j++;
	}
	return (0);
}
